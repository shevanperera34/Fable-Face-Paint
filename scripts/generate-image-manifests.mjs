import fg from "fast-glob";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outFile = path.join(root, "src", "generated", "imageManifests.ts");
const assetsDir = path.join(root, "src", "assets");

const exts = "{png,jpg,jpeg,webp,avif,gif,svg}";

function toImportPath(relativeFromGenerated) {
  return relativeFromGenerated.split(path.sep).join("/");
}

function esc(p) {
  return p.replace(/\\/g, "/");
}

async function collect(relGlob) {
  const files = await fg(relGlob, {
    cwd: assetsDir,
    onlyFiles: true,
  });
  return files.map((f) => esc(f)).sort((a, b) => a.localeCompare(b));
}

function uniqueVar(prefix, index) {
  return `${prefix}_${index}`;
}

async function main() {
  const vertical = await collect(`Event pics_/vertical/*.${exts}`);
  const horizontal = await collect(`Event pics_/horizontal/*.${exts}`);
  const serviceFiles = await collect(`srevice assets/*/Assets/*.${exts}`);

  const lines = [];
  lines.push("/* eslint-disable */");
  lines.push("/* Auto-generated by scripts/generate-image-manifests.mjs — do not edit. */");
  lines.push("");

  let idx = 0;
  const verticalImports = vertical.map((rel, i) => {
    const v = uniqueVar("v", i);
    const imp = toImportPath(path.join("..", "assets", ...rel.split("/")));
    lines.push(`import ${v} from "${imp}";`);
    return v;
  });

  idx = 0;
  const horizontalImports = horizontal.map((rel, i) => {
    const v = uniqueVar("h", i);
    const imp = toImportPath(path.join("..", "assets", ...rel.split("/")));
    lines.push(`import ${v} from "${imp}";`);
    return v;
  });

  const serviceImports = serviceFiles.map((rel, i) => {
    const v = uniqueVar("s", i);
    const imp = toImportPath(path.join("..", "assets", ...rel.split("/")));
    lines.push(`import ${v} from "${imp}";`);
    return v;
  });

  lines.push("");
  lines.push(`export const eventPicVerticalUrls: string[] = [${verticalImports.join(", ")}].sort((a, b) => a.localeCompare(b));`);
  lines.push("");
  lines.push(`export const eventPicHorizontalUrls: string[] = [${horizontalImports.join(", ")}].sort((a, b) => a.localeCompare(b));`);
  lines.push("");
  lines.push(
    "export const allEventPicsSorted: string[] = [...eventPicVerticalUrls, ...eventPicHorizontalUrls].sort((a, b) => a.localeCompare(b));"
  );
  lines.push("");

  lines.push("export type ServiceAssetEntry = { key: string; url: string };");
  lines.push("");
  lines.push("export const serviceAssetEntries: ServiceAssetEntry[] = [");
  for (let i = 0; i < serviceFiles.length; i++) {
    const rel = serviceFiles[i];
    const logicalKey = `src/assets/${rel}`;
    const impVar = serviceImports[i];
    lines.push(`  { key: ${JSON.stringify(logicalKey)}, url: ${impVar} },`);
  }
  lines.push("].sort((a, b) => a.key.localeCompare(b.key));");
  lines.push("");
  lines.push(`export const serviceAssetModuleRecord: Record<string, string> = Object.fromEntries(
  serviceAssetEntries.map((e) => [e.key, e.url])
);`);
  lines.push("");

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, lines.join("\n") + "\n", "utf8");
  console.log(`Wrote ${path.relative(root, outFile)} (${vertical.length} vertical, ${horizontal.length} horizontal, ${serviceFiles.length} service images).`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
